function sanitise(raw: string): string {
  return raw.toUpperCase().replace(/\s+/g, " ").trim();
}

export function getCouncillorForWard(rawName: string | null | undefined): Councillor | undefined {
  if (!rawName) return undefined;
  const needle = sanitise(rawName);
  const exact = councillorList.find((c) => sanitise(c.wardName) === needle);
  if (exact) return exact;
  return councillorList.find((c) => sanitise(c.wardName).includes(needle) || needle.includes(sanitise(c.wardName)));
}
